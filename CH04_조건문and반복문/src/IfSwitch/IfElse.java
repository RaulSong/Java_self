package IfSwitch;

public class IfElse {

	public static void main(String[] args) {
		// ***if-else문***
		int score = 74;
		
		if(score>=90) {
			System.out.println("점수가 90점보다 높습니다.");
			System.out.println("A등급입니다.");
		} else {
			System.out.println("점수가 90점보다 작습니다.");
			System.out.println("B등급입니다.");
		}
	}
}
